package com.lxt.common.plugin.workflow.bpmn.entity3;

public class ParameterIn {
	private String subvar;
	private String var;
	private String expr;
	private String lang;

	public String getSubvar() {
		return subvar;
	}

	public void setSubvar(String subvar) {
		this.subvar = subvar;
	}

	public String getVar() {
		return var;
	}

	public void setVar(String var) {
		this.var = var;
	}

	public String getExpr() {
		return expr;
	}

	public void setExpr(String expr) {
		this.expr = expr;
	}

	public String getLang() {
		return lang;
	}

	public void setLang(String lang) {
		this.lang = lang;
	}

}
